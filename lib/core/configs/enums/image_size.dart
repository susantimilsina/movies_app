/// Enum for image sizes from The TMDB API configurations
///
/// See: https://developers.themoviedb.org/3/configuration/get-api-configuration
enum ImageSize {
  /// Image width of 300px
  w300,

  /// Image width of 780px
  w780,

  /// Image width of 1280px
  w1280,

  /// Image width of 45px
  w45,

  /// Image width of 92px
  w92,

  /// Image width of 154px
  w154,

  /// Image width of 185px
  w185,

  /// Image width of 500px
  w500,

  /// Image width of 342px
  w342,

  /// Image height of 632px
  h632,

  /// Original image size
  original,
}
